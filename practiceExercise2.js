// Exercise 2: Simple Calculator
const numberOne = 32;
const numberTwo = 20;

console.log(`${numberOne} + ${numberTwo} = ${numberOne + numberTwo}`);
console.log(`${numberOne} - ${numberTwo} = ${numberOne - numberTwo}`);
console.log(`${numberOne} * ${numberTwo} = ${numberOne * numberTwo}`);
console.log(`${numberOne} / ${numberTwo} = ${numberOne / numberTwo}`);
console.log(`${numberOne} % ${numberTwo} = ${numberOne % numberTwo}`); 

// Note: modulo (%) use to determine if the result is even or odd
// Example :
const num = 10;
if (num % 2 === 0) {
    console.log("Even number");
} else {
    console.log("Odd number");
}